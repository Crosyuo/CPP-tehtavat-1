#include <iostream>
using namespace std;

int main()
{
    cout << "Int koko: " << sizeof(int) << endl;
    cout << "Unsigned int koko: " << sizeof(unsigned int) << endl;
    cout << "Float koko: " << sizeof(float) << endl;
    cout << "Double koko: " << sizeof(double) << endl;
    cout << "Char koko: " << sizeof(char) << endl;
    cout << "Bool koko: " << sizeof(bool) << endl;
}